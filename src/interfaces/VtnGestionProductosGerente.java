    public void llenaCategoria()
    {

        Categorias categorias = new Categorias();
        cat = categorias.consultaMultipleVec(con);
        if (cat != null)
        {
            jcategoria.removeAllItems();;
            for (int i = 0; i < cat.length; i++)
            {
                jcategoria.addItem(cat[i].getNombre_categoria());
            }
        } else
        {
            Mensaje.error(this, "no carga estados");
        }
    }

    public int ExtraeId(String nombre)
    {
        for (int i = 0; i < cat.length; i++)
        {
            if (cat[i].getNombre_categoria().equals(nombre))
            {
                System.out.println("id_categorias" + cat[i].getId_categoria());
                return cat[i].getId_categoria();
            }
        }
        System.out.println("Retornando 0");
        return 0;
    }

    public void nuevo()
    {
        if (edicion)
        {
            if (Mensaje.pregunta(this, "¿Seguro que quiere dar de alta?") == JOptionPane.YES_OPTION)
            {
                Productos pro = new Productos(0,
                        txtnnomProducto.getText(),
                        ExtraeId(String.valueOf(jcategoria.getSelectedItem())),
                        txtDescripcion.getText(),
                        Float.parseFloat(txtPrecio_fabricacion.getText()),
                        Float.parseFloat(txtPrecio_ventas.getText()));
                pro.inserta(this, pro, con);
                llenaTabla();
                edicion = false;
                cjb.ci.CtrlInterfaz.habilita(edicion, txtDescripcion, txtnnomProducto, txtPrecio_ventas, jcategoria, txtPrecio_fabricacion);
                cjb.ci.CtrlInterfaz.habilita(!edicion, btneliminar, btnmodificar, btncancelar);
                btnnuevo.setText("Nuevo");
            }

        } else
        {
            edicion = true;
            CtrlInterfaz.selecciona(txtnnomProducto);
            cjb.ci.CtrlInterfaz.habilita(!edicion, btneliminar, btnmodificar);
            cjb.ci.CtrlInterfaz.habilita(edicion, jcategoria, txtDescripcion, txtnnomProducto, txtPrecio_ventas);
            cjb.ci.CtrlInterfaz.limpia(txtDescripcion, txtnnomProducto, txtPrecio_fabricacion, txtPrecio_ventas);
            btnnuevo.setText("Aceptar");
        }
    }

    public void eliminar()
    {
        try
        {
            if (Mensaje.pregunta(this, "¿Seguro que quiere eliminar a " + jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 1) + "?") == JOptionPane.YES_OPTION)
            {
                Productos pro = new Productos();
                pro.elimina(this, Integer.parseInt(String.valueOf(jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 0))), con);
                llenaTabla();

            }

        } catch (Exception e)
        {
            Mensaje.error(this, "Selecione un elemento de la tabla");
            System.out.println(e.toString());
        }
    }

