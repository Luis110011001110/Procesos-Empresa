    private void jBLogActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jBLogActionPerformed
    {//GEN-HEADEREND:event_jBLogActionPerformed
        emp = new Empleados(jTFUser.getText(), jPFPass.getText());
        emp = (Empleados) emp.consultaUnicausurio(jTFUser.getText(), con);
        if (emp.getCorreo() == null) {
            cjb.ci.CtrlInterfaz.selecciona(jTFUser);
            Mensaje.error(this, "Usuario no encontrado");
        } else {
            if (emp.getPassword().equals(jPFPass.getText())) {
                switch (emp.getId_tipousuario()) {
                    case 1:
                        new VtnMainAdmin().setVisible(true);
                        break;
                    case 2:
                        new VtnMainGerente().setVisible(true);
                        break;
                    case 3:
                        new VtnMainEmpleado().setVisible(true);
                        break;
                }
                this.dispose();
            }else{
                cjb.ci.CtrlInterfaz.selecciona(jPFPass);
                Mensaje.error(this, "Contrase침a equivocada");
            }
        }
        System.out.println(emp.getNombre() + "tipo" + emp.getId_tipousuario());

    }//GEN-LAST:event_jBLogActionPerformed

