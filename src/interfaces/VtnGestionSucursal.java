    public void llenaEstados()
    {

        Estados est1 = new Estados();
        est = est1.consultaMultipleVec(con);
        if (est != null)
        {
            jcestado.removeAllItems();;
            for (int i = 0; i < est.length; i++)
            {
                jcestado.addItem(est[i].getNombre_estado());
            }
        } else
        {
            Mensaje.error(this, "no carga estados");
        }
    }

    public int ExtraeId(String nombre)
    {
        for (int i = 0; i < est.length; i++)
        {
            if (est[i].getNombre_estado().equals(nombre))
            {
                return est[i].getId_estado();
            }
        }
        return 0;
    }

    public void elimina()
    {
        try
        {
            if (Mensaje.pregunta(this, "¿Seguro que quiere eliminar a " + jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 1) + "?") == JOptionPane.YES_OPTION)
            {
                if (String.valueOf(jctipo.getSelectedItem()).equals("Primaria"))
                {
                    Mensaje.error(this, "!!No se puede eliminar esta sucursal!!");
                } else
                {
                    Sucursal suc = new Sucursal();
                    suc.elimina(this, Integer.parseInt(String.valueOf(jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 0))), con);
                    llenaTabla();
                }
            }

        } catch (Exception e)
        {
            Mensaje.error(this, "Selecione un elemento de la tabla");
            System.out.println(e.toString());
        }
    }

    public void modifica()
    {
        try
        {
            if (edicion)
            {
                if (Mensaje.pregunta(this, "¿Seguro que quiere dar de editar a " + jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 1) + "?") == JOptionPane.YES_OPTION)
                {
                    Sucursal suc = new Sucursal(Integer.parseInt(String.valueOf(jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 0))),
                            jTFNombreSucursal.getText(),
                            jctipo.getSelectedIndex() + 1,
                            ExtraeId(String.valueOf(jcestado.getSelectedItem())),
                            jTFDireccion.getText(),
                            jTFTelefono.getText(),
                            jTFCorreo.getText());
                    suc.modifica(this, suc, con);
                    llenaTabla();
                    edicion = false;
                    cjb.ci.CtrlInterfaz.habilita(edicion, jTFNombreSucursal, jTFTelefono, jctipo, jcestado, jTFDireccion, jTFCorreo);
                    cjb.ci.CtrlInterfaz.habilita(!edicion, btnnuevo, btncancelar, btneliminar, btneditar);
                    btnnuevo.setText("Nuevo");
                }

            } else
            {
                if (null == jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 0))
                {

                }
                edicion = true;
                cjb.ci.CtrlInterfaz.habilita(edicion, jTFNombreSucursal, jTFTelefono, jctipo, jcestado, jTFDireccion, jTFCorreo);
                cjb.ci.CtrlInterfaz.habilita(!edicion, btnnuevo, btneliminar);
                btnnuevo.setText("Aceptar");
            }
        } catch (Exception e)
        {
            Mensaje.error(this, "Selecione un elemento de la tabla");
            System.out.println(e.toString());
        }
    }

