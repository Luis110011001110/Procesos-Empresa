    private void btnnuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnuevoActionPerformed
        if (edicion)
        {
            if (Mensaje.pregunta(this, "¿Seguro que quiere dar de alta?") == JOptionPane.YES_OPTION)
            {
                Proveedores prov = new Proveedores(0,
                        txtNombre.getText(),
                        txtDescripcion.getText(),
                        txtTelefono.getText(),
                        txtCorreo.getText(),
                        txtWeb.getText());
                prov.inserta(this, prov, con);
                llenaTabla();
                edicion = false;
                cjb.ci.CtrlInterfaz.habilita(edicion, txtTelefono, txtNombre, txtWeb);
                cjb.ci.CtrlInterfaz.habilita(!edicion, btnnuevo, btncancelar, btneliminar, btneditar);
                btnnuevo.setText("Nuevo");
            }

        } else
        {
            edicion = true;
            CtrlInterfaz.selecciona(txtNombre);
            cjb.ci.CtrlInterfaz.habilita(edicion, txtTelefono, txtNombre, txtWeb);
            cjb.ci.CtrlInterfaz.habilita(!edicion, btneditar, btneliminar);
            cjb.ci.CtrlInterfaz.limpia(txtTelefono, txtNombre, txtWeb);
            btnnuevo.setText("Aceptar");
        }
    }//GEN-LAST:event_btnnuevoActionPerformed

